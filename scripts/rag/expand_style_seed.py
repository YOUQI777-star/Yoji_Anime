    print(f"现有 anime_id 集合大小: {len(existing_ids)}")

    # 解析新行，用 csv.reader 正确处理带逗号的字段
    new_rows_to_add = []
    seen_new_ids = set()

    for line in NEW_ROWS_RAW.strip().splitlines():
        line = line.strip()
        if not line:
            continue
        # 用 csv.reader 解析，防止字段内有逗号出错
        parsed = list(csv.reader(io.StringIO(line)))[0]
        if len(parsed) != 8:
            print(f"警告：字段数不对（{len(parsed)}），跳过: {line[:60]}...")
            continue
        anime_id = parsed[0].strip()
        if anime_id in existing_ids:
            print(f"跳过（已存在）: {anime_id} {parsed[1]}")
            continue
